first_number = float(input("Enter first number: "))
sign = input("Enter sign: ")
second_number = float(input("Enter second number: "))
if sign == "+":
    resolt = print(first_number + second_number)
elif sign == "-":
    resolt = print(first_number - second_number)
elif sign == "/":
    resolt = print(first_number / second_number)
elif sign == "*":
    resolt = print(first_number * second_number)